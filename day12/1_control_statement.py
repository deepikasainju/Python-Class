# break , continue and pass are the control statements that alter the normal flow of the program

## break
for i in range(10):
    if i==4:
        break
    print(i)

n=0
while n<10:
    if i==4:
        break
    print(n)
    n+=1


## continue
for i in range(10):
    if i==0:
        continue
    print(i)

n=0
while n<10:
    if n==4:
        continue
    print(n)
    n+=1


